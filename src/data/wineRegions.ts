/** Flat list of all known regions, derived from WINE_REGIONS_BY_COUNTRY. */
export const ALL_WINE_REGIONS: string[] = [
    ...new Set(Object.values(WINE_REGIONS_BY_COUNTRY).flat()),
].sort((a, b) => a.localeCompare(b));

/** Returns regions for a selected country, or all regions when no country is set. */
export function getRegionsForCountry(country?: string): string[] {
    if (country && WINE_REGIONS_BY_COUNTRY[country]) {
        return WINE_REGIONS_BY_COUNTRY[country];
    }
    return ALL_WINE_REGIONS;
}
